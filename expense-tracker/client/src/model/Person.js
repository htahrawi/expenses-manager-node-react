class Person {
    id;
    name;
    email;
    phone;
    job;
    age;
    salary;
    password;


    constructor(id, name, email, phone, job, age, salary, password) {
        this.id = id;
        this.name = name;
        this.email = email;
        this.phone = phone;
        this.job = job;
        this.age = age;
        this.salary = salary;
        this.password = password;
    }

}
export default Person;